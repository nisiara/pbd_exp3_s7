  FUNCTION fn_calcular_descuento(p_edad_paciente NUMBER, p_costo_multa NUMBER) 
    RETURN NUMBER IS

    r_descuento_edad Porc_Descto_3ra_Edad%ROWTYPE;

    CURSOR c_edades_descuento IS
      SELECT
        anno_ini DESDE
        ,anno_ter HASTA
        ,porcentaje_descto DESCUENTO
      FROM Porc_Descto_3ra_Edad;

    BEGIN

      FOR f_edad IN c_edades_descuento LOOP
        r_descuento_edad.anno_ini := f_edad.desde;
        r_descuento_edad.anno_ter := f_edad.hasta;
        r_descuento_edad.porcentaje_descto := f_edad.descuento;

        IF p_edad_paciente BETWEEN r_descuento_edad.anno_ini AND r_descuento_edad.anno_ter
          THEN
            PCK_MOROSOS.vp_valor_descuento := p_costo_multa - (p_costo_multa * (r_descuento_edad.porcentaje_descto / 100));
              EXIT;
        ELSE
          PCK_MOROSOS.vp_valor_descuento := p_costo_multa;
      END IF;      
      END LOOP;

    RETURN PCK_MOROSOS.vp_valor_descuento;

  END fn_calcular_descuento;

  
