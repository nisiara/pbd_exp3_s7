  FUNCTION fn_calcular_multa(p_atencion_id NUMBER, p_especialidad VARCHAR2) 
    RETURN NUMBER IS

    TYPE VARRAY_MULTA_DIA_EXTRA IS VARRAY(7) OF NUMBER;
    va_multa_dia_extra VARRAY_MULTA_DIA_EXTRA := VARRAY_MULTA_DIA_EXTRA(1200, 1300, 1700, 1900, 1100, 2000, 2300);

    BEGIN
      SELECT
        NVL(
          CASE
            WHEN p_especialidad = 'Medicina General' THEN (pa.fecha_pago - pa.fecha_venc_pago) * va_multa_dia_extra(1) 
            WHEN p_especialidad = 'Traumatologia' THEN (pa.fecha_pago - pa.fecha_venc_pago) * va_multa_dia_extra(2)          
            WHEN p_especialidad IN ('Neurologia', 'Pediatria') THEN (pa.fecha_pago - pa.fecha_venc_pago) * va_multa_dia_extra(3)   
            WHEN p_especialidad = 'Oftalmologia' THEN (pa.fecha_pago - pa.fecha_venc_pago) * va_multa_dia_extra(4)   
            WHEN p_especialidad = 'Geriatria' THEN (pa.fecha_pago - pa.fecha_venc_pago) * va_multa_dia_extra(5)          
            WHEN p_especialidad IN ('Ginecologia', 'Gastroenterologia') THEN (pa.fecha_pago - pa.fecha_venc_pago) * va_multa_dia_extra(6)          
            WHEN p_especialidad = 'Dermatologia' THEN (pa.fecha_pago - pa.fecha_venc_pago) * va_multa_dia_extra(7)
          END 
        , 0) 
        INTO PCK_MOROSOS.vp_valor_multa
      FROM 
        Atencion a
        INNER JOIN PAGO_ATENCION pa ON a.ate_id = pa.ate_id
      WHERE
        a.ate_id = p_atencion_id;

      RETURN PCK_MOROSOS.vp_valor_multa;

  END fn_calcular_multa;


