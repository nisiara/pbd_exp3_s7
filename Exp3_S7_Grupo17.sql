  FUNCTION fn_mensaje_observacion(p_edad_paciente NUMBER)
    RETURN VARCHAR2 IS
    v_observacion VARCHAR2(100);

    BEGIN
      IF p_edad_paciente > 70 THEN
        v_observacion := 
          'Paciente tenía ' || p_edad_paciente || ' a la fecha de atención. Se aplicó descuento paciente mayor a 70 años.';
      END IF;

    RETURN v_observacion;
  END fn_mensaje_observacion;


