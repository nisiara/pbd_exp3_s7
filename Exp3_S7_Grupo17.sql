        ,p.pnombre || ' ' || p.snombre || ' ' || p.apaterno || ' ' || p.amaterno NOMBRE_PACIENTE
        ,a.ate_id
        ,pa.fecha_venc_pago
        ,pa.fecha_pago
        ,pa.fecha_pago - pa.fecha_venc_pago DIAS_MOROSIDAD
        ,a.costo
        ,TRUNC(p_anno_proceso - EXTRACT(YEAR FROM fecha_nacimiento)) EDAD

      FROM
        Paciente p
        INNER JOIN Atencion a ON p.pac_run = a.pac_run
        INNER JOIN Pago_Atencion pa ON a.ate_id = pa.ate_id

      WHERE
        EXTRACT(YEAR FROM pa.fecha_pago) = p_anno_proceso
        AND EXTRACT(YEAR FROM pa.fecha_venc_pago) = p_anno_proceso
        AND pa.fecha_pago - pa.fecha_venc_pago > 0
      ORDER BY
        pa.fecha_venc_pago ASC;

    BEGIN
      EXECUTE IMMEDIATE 'TRUNCATE TABLE Pago_Moroso';

      FOR f_moroso IN c_pacientes_morosos LOOP

        r_resumen_morosos.pac_run := f_moroso.pac_run;
        r_resumen_morosos.pac_dv_run := f_moroso.dv_run;
        r_resumen_morosos.pac_nombre := INITCAP(f_moroso.nombre_paciente);
        r_resumen_morosos.ate_id := f_moroso.ate_id;
        r_resumen_morosos.fecha_venc_pago := TO_CHAR(f_moroso.fecha_venc_pago, 'DD/MM/YYYY');
        r_resumen_morosos.fecha_pago := TO_CHAR(f_moroso.fecha_pago, 'DD/MM/YYYY');
        r_resumen_morosos.dias_morosidad := f_moroso.dias_morosidad;

      --Espcialidad Atencion
        r_resumen_morosos.especialidad_atencion := PCK_MOROSOS.FN_TRAER_ESPECIALIDAD(r_resumen_morosos.ate_id);

      --Calcula el monto de la deuda sin aplicar descuento
        r_resumen_morosos.costo_atencion := f_moroso.costo; 
        v_monto_multa_sin_descuento := PCK_MOROSOS.FN_CALCULAR_MULTA(r_resumen_morosos.ate_id, r_resumen_morosos.especialidad_atencion);

      --Calcula el descuento de la deuda dependiendo del rango de edad del paciente
        v_edad_paciente := f_moroso.edad;
        r_resumen_morosos.monto_multa := PCK_MOROSOS.FN_CALCULAR_DESCUENTO(v_edad_paciente, v_monto_multa_sin_descuento);

      -- Retorna el mensaje dependiendo si tiene descuento o no
        r_resumen_morosos.observacion := PCK_MOROSOS.FN_MENSAJE_OBSERVACION(v_edad_paciente);

        INSERT INTO Pago_Moroso VALUES r_resumen_morosos;
      END LOOP;

      COMMIT;

  END prc_resumen_morosos;


